  findMatchingPokemon(name) {
    const matches = [];
    for( let mon in this.assets) {
      if( this.assets[mon].name.toLowerCase().indexOf(name) !== -1 ) {
        matches.push(this.assets[mon].name);
      }
    };
    return matches;
  }

